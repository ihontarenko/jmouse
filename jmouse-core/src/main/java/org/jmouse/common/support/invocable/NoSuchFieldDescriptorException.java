package org.jmouse.common.support.invocable;

public class NoSuchFieldDescriptorException extends RuntimeException {

    public NoSuchFieldDescriptorException() {
        super();
    }

    public NoSuchFieldDescriptorException(String message) {
        super(message);
    }

    public NoSuchFieldDescriptorException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoSuchFieldDescriptorException(Throwable cause) {
        super(cause);
    }

}
