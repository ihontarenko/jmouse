package svit.support.invocable;

public class NoSuchMethodDescriptorException extends RuntimeException {

    public NoSuchMethodDescriptorException() {
        super();
    }

    public NoSuchMethodDescriptorException(String message) {
        super(message);
    }

    public NoSuchMethodDescriptorException(String message, Throwable cause) {
        super(message, cause);
    }

    public NoSuchMethodDescriptorException(Throwable cause) {
        super(cause);
    }

}
